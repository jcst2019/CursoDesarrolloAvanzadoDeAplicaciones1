package com.idat.desarrolloavanzadodeaplicaciones1.ASIGNACION;

/**
 *
 * @author Solaris
 * Este programa demuestra cómo asignar y reasignar valores a variables.
 */
public class Asignacion1 {
    
     public static void main(String[] args) {
        // Declaramos una variable entera y le asignamos un valor inicial
        int edad = 20; // Asignamos 20 como el valor inicial de la variable 'edad'
        System.out.println("Edad inicial: " + edad); // Mostramos el valor inicial de la variable

        // Reasignamos un nuevo valor a la misma variable
        edad = 25; // Cambiamos el valor de 'edad' a 25
        System.out.println("Edad después de la reasignación: " + edad); // Mostramos el nuevo valor de la variable
    }
    
    
}
