package com.idat.desarrolloavanzadodeaplicaciones1.ASIGNACION;

/**
 *
 * @author Solaris
 * Este programa muestra cómo asignar valores a variables utilizando operaciones matemáticas.
 */
public class Asignacion3 {
    
       public static void main(String[] args) {
        // Declaramos una variable y le asignamos un valor inicial
        int numero = 10; // Asignamos 10 a la variable 'numero'
        System.out.println("Valor inicial de numero: " + numero); // Mostramos el valor inicial

        // Modificamos el valor de la variable usando operaciones matemáticas
        numero = numero + 5; // Sumamos 5 al valor actual de 'numero'
        System.out.println("Después de sumar 5: " + numero); // Mostramos el nuevo valor

        numero = numero * 2; // Multiplicamos el valor actual de 'numero' por 2
        System.out.println("Después de multiplicar por 2: " + numero); // Mostramos el resultado

        numero = numero - 8; // Restamos 8 al valor actual de 'numero'
        System.out.println("Después de restar 8: " + numero); // Mostramos el valor final
    }
    
}
