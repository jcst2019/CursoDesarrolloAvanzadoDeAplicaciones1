package com.idat.desarrolloavanzadodeaplicaciones1.OPERADORES_ARITMETICOS;

/**
 *
 * @author Solaris
 * Este programa realiza operaciones aritméticas con números decimales
 */
public class Operadores2 {
    
        public static void main(String[] args) {
        // Declaramos dos variables de tipo double
        double numero1 = 7.5; // Asignamos el valor 7.5 a 'numero1'
        double numero2 = 2.3; // Asignamos el valor 2.3 a 'numero2'

        // Realizamos las operaciones básicas: suma, resta, multiplicación, división
        double suma = numero1 + numero2; // Sumamos los dos números decimales
        double resta = numero1 - numero2; // Restamos 'numero2' de 'numero1'
        double multiplicacion = numero1 * numero2; // Multiplicamos los dos números
        double division = numero1 / numero2; // Dividimos 'numero1' entre 'numero2'

        // Mostramos los resultados de las operaciones
        System.out.println("Suma: " + suma); // Mostramos el resultado de la suma
        System.out.println("Resta: " + resta); // Mostramos el resultado de la resta
        System.out.println("Multiplicación: " + multiplicacion); // Mostramos el resultado de la multiplicación
        System.out.println("División: " + division); // Mostramos el resultado de la división
    }
    
}
