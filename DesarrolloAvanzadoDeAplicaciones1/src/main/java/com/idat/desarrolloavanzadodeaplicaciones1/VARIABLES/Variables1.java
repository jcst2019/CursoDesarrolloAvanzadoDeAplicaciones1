package com.idat.desarrolloavanzadodeaplicaciones1.VARIABLES;

/**
 *
 * @author Solaris
 * Este programa demuestra cómo declarar variables, asignarles valores y mostrarlas en la consola.
 */
public class Variables1 {
    
   public static void main(String[] args) {
        // Declaramos variables de diferentes tipos
        int edad = 25; // Variable entera para almacenar la edad
        double salario = 3500.50; // Variable de tipo double para el salario
        char inicial = 'J'; // Variable de tipo char para una inicial
        String nombre = "Carlos"; // Variable de tipo String para el nombre
        boolean esEmpleado = true; // Variable booleana para verificar si es empleado

        // Mostramos los valores de las variables en la consola
        System.out.println("Nombre: " + nombre);
        System.out.println("Edad: " + edad);
        System.out.println("Salario: " + salario);
        System.out.println("Inicial del nombre: " + inicial);
        System.out.println("¿Es empleado? " + esEmpleado);
    }
}
